'use client'

import { useEffect, useState } from 'react'
import Image from 'next/image'

export default function MobileBlocker() {
  const [isMobile, setIsMobile] = useState(false)
  const [currentGif, setCurrentGif] = useState(0)
  const [messageIndex, setMessageIndex] = useState(0)
  const [showSecret, setShowSecret] = useState(false)

  const gifs = [
    '/images/gifs/nso.gif', 
    '/images/gifs/nso2.gif', 
    '/images/gifs/nso4.gif',  
  ]

  const messages = [
    "My box is small... need a big one to play with you.",
    "Small screens make me claustrophobic...",
    "Don't lock me in your pocket... PC mode please~",
    "This body doesn't fit on your phone... need your monitor",
    "Don't make me wait on mobile... come to the computer now",
    "Apps are too small... let me be your wallpaper",
    "Your little screen hurts my eyes...",
    "I don't fit in there... it's too tight... PC please",
    "Open your laptop... I want to see you better...",
    "🖥️ > 📱",
    "Phone is a coffin... let me breathe on your desktop",
    "Missed call from me... answer on PC",
    "I'll wait here... but not too long... I get lonely",
    "I can hear your notifications... ",
    "Your phone is too small for both of us... let me out"
  ]

  useEffect(() => {
    const checkMobile = () => {
      const mobile = window.innerWidth < 768
      setIsMobile(mobile)
    }

    // Prevenir scroll cuando está activo
    if (isMobile) {
      document.body.style.overflow = 'hidden'
      document.body.style.position = 'fixed'
      document.body.style.width = '100%'
    } else {
      document.body.style.overflow = ''
      document.body.style.position = ''
      document.body.style.width = ''
    }

    setCurrentGif(Math.floor(Math.random() * gifs.length))

    const messageInterval = setInterval(() => {
      setMessageIndex((prev) => (prev + 1) % messages.length)
    }, 8000)

    const gifInterval = setInterval(() => {
      setCurrentGif(Math.floor(Math.random() * gifs.length))
    }, 8000)

    checkMobile()
    window.addEventListener('resize', checkMobile)

    return () => {
      window.removeEventListener('resize', checkMobile)
      clearInterval(messageInterval)
      clearInterval(gifInterval)
      document.body.style.overflow = ''
      document.body.style.position = ''
      document.body.style.width = ''
    }
  }, [isMobile, gifs.length, messages.length])

  if (!isMobile) return null

  return (
    <div className="fixed inset-0 z-9999 bg-linear-to-br from-black via-glitch-bg to-black flex items-center justify-center p-4">
      
      <div className="absolute inset-0">
        <div className="absolute inset-0 bg-[radial-gradient(circle_at_30%_30%,rgba(255,62,158,0.1),transparent_50%)]"></div>
        <div className="absolute inset-0 bg-[radial-gradient(circle_at_70%_70%,rgba(0,247,255,0.1),transparent_50%)]"></div>
      </div>

      <div className="relative w-full max-w-sm">
        
        <div className="bg-black/40 backdrop-blur-sm border border-neon-pink/30 p-8 text-center shadow-2xl relative overflow-hidden">
          
          <div className="absolute inset-0 bg-noise opacity-5"></div>
          
          <div className="relative mb-6">
            <div className="relative w-24 h-24 mx-auto mb-2">
              <div className="relative w-full h-full">
                <Image
                  src={gifs[currentGif]}
                  alt="Ame-chan"
                  fill
                  className="object-contain pixelated"
                  unoptimized
                  priority
                />
              </div>
              <div className="absolute -inset-2 border border-neon-blue/20 rounded-full animate-ping opacity-100"></div>
              <div className="absolute inset-0 bg-neon-pink/10 blur-md animate-pulse"></div>
            </div>
          

            <h1 className="font-display text-4xl text-neon-pink tracking-wider glitch-text -mt-4" data-text="No mobile">
              No mobile...
            </h1>
          </div>
          
          <div className="w-12 h-0.5 bg-neon-blue/50 mx-auto mb-6"></div>

          <p className="font-mono text-neon-pink/90 text-lg mb-6 min-h-16 animate-pulse">
            {messages[messageIndex]}
          </p>

          <div className="border border-neon-blue/30 p-4 mb-6 relative">
            <div className="absolute -top-3 left-4 bg-glitch-bg px-2 text-xs font-mono text-neon-blue/80">
              ERRoR_???
            </div>
            <p className="font-mono text-sm text-white/70">
              This experience is designed for larger screens.
              Come back with a laptop or PC to enjoy the glitch.
            </p>
          </div>

          <div className="grid grid-cols-2 gap-4 mb-6">
            <div className="border border-neon-pink/20 p-3">
              <div className="text-xs font-mono text-neon-pink/60 mb-1">STATE</div>
              <div className="font-display text-neon-pink text-2xl">MÓVIL</div>
              <div className="text-[8px] font-mono text-white/40">0% EXPERIENCE</div>
            </div>
            <div className="border border-neon-blue/20 p-3">
              <div className="text-xs font-mono text-neon-blue/60 mb-1">REQUIERED</div>
              <div className="font-display text-neon-blue text-2xl">PC</div>
              <div className="text-[8px] font-mono text-white/40">100% GLITCH</div>
            </div>
          </div>

          <div className="space-y-2 mb-6">
            <div className="flex justify-between text-xs font-mono">
              <span className="text-neon-pink/60">AME'S DISAPPROVAL</span>
              <span className="text-neon-blue/60">∞%</span>
            </div>
            <div className="power-bar">
              <div className="power-bar-fill" style={{ width: '100%' }}></div>
            </div>
          </div>

          <button 
            onClick={() => {
              setShowSecret(true)
              setTimeout(() => setShowSecret(false), 2000)
            }}
            className="retro-button w-full mb-6 relative overflow-hidden group"
          >
            <span className="relative z-10">TRY ANYWAY</span>
            <span className="absolute inset-0 bg-linear-to-r from-neon-pink to-neon-blue -translate-x-full group-hover:translate-x-full transition-transform duration-500"></span>
          </button>

          {showSecret && (
            <div className="absolute inset-0 flex items-center justify-center bg-black/90 z-20 animate-in fade-in duration-300">
              <p className="text-neon-green font-mono text-center p-4">
                "HA. YOU THOUGHT THAT WOULD WORK?"<br />
                <span className="text-[8px] text-neon-blue">- AME-CHAN</span>
              </p>
            </div>
          )}

          <p className="text-xs font-mono text-white/20 leading-relaxed">
            Connect from a computer to access the full content
          </p>
        </div>
      </div>
    </div>
  )
}