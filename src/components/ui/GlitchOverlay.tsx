'use client'

import { useGlitch } from '@/context/GlitchContext'
import { useEffect, useState } from 'react'

export default function GlitchOverlay() {
  const { isGlitching, glitchIntensity } = useGlitch()
  const [vhsNoise, setVhsNoise] = useState('')
  const [tabReturnMessage, setTabReturnMessage] = useState('')

  useEffect(() => {
    if (isGlitching) {
      const lines = Array.from({ length: 30 }, (_, i) => (
        `.glitch-line-${i} {
          position: fixed;
          top: ${Math.random() * 100}%;
          left: 0;
          width: 100%;
          height: ${Math.random() * 6 + 1}px;
          background: rgba(${Math.random() * 255}, ${Math.random() * 100}, ${Math.random() * 255}, 0.7);
          z-index: 99999;
          animation: glitch-scan ${Math.random() * 1.5 + 0.5}s linear infinite;
          pointer-events: none;
        }`
      )).join('\n')

      setVhsNoise(lines)

      const messages = [
        'Where were you?',
        'eh..?',
        'INSERT DISC',
        'BUFFER OVERFLOW',
        'I WAS WAITING...',
        'DID YOU MISS ME?',
        'DON\'T LEAVE ME AGAIN',
        'ERROR: USER NOT FOUND',
        '...'
      ]
      setTabReturnMessage(messages[Math.floor(Math.random() * messages.length)])
    }
  }, [isGlitching])

if (!isGlitching) return null

return (
  <div className="fixed inset-0 z-9999 animate-in fade-in duration-100">
    <style>{`
      @keyframes glitch-scan {
        0% { transform: translateY(-100%); }
        100% { transform: translateY(100vh); }
      }

      @keyframes vhs-roll {
        0% { transform: skew(0deg) scale(1); filter: hue-rotate(0deg); }
        25% { transform: skew(5deg) scale(1.02); filter: hue-rotate(90deg); }
        50% { transform: skew(-5deg) scale(0.98); filter: hue-rotate(180deg); }
        75% { transform: skew(3deg) scale(1.01); filter: hue-rotate(270deg); }
        100% { transform: skew(0deg) scale(1); filter: hue-rotate(360deg); }
      }

      .glitch-active {
        animation: vhs-roll ${0.5 / glitchIntensity}s infinite !important;
      }
    `}</style>

    <div 
      className="fixed inset-0 z-99998 pointer-events-none"
      style={{
        background: `
          repeating-linear-gradient(
            0deg,
            rgba(255, 0, 100, ${0.15 * glitchIntensity}) 0px,
            rgba(0, 255, 255, ${0.15 * glitchIntensity}) 2px,
            transparent 4px
          ),
          repeating-linear-gradient(
            90deg,
            rgba(255, 255, 0, ${0.1 * glitchIntensity}) 0px,
            rgba(255, 0, 255, ${0.1 * glitchIntensity}) 2px,
            transparent 4px
          )
        `,
        mixBlendMode: 'overlay',
        animation: `glitch-scan ${1.5 / glitchIntensity}s linear infinite`
      }}
    />

    <style>{vhsNoise}</style>
    {Array.from({ length: 30 }).map((_, i) => (
      <div
        key={i}
        className="fixed pointer-events-none z-99999"
        style={{
          top: `${Math.random() * 100}%`,
          left: 0,
          width: '100%',
          height: `${Math.random() * 6 + 1}px`,
          background: `rgba(${Math.random() * 255}, ${Math.random() * 100}, ${Math.random() * 255}, 0.7)`,
          animation: `glitch-scan ${Math.random() * 1.5 + 0.5}s linear infinite`,
          boxShadow: `0 0 10px rgba(${Math.random() * 255}, 0, ${Math.random() * 255}, 0.8)`
        }}
      />
    ))}

    <div 
      className="fixed inset-0 z-99997 pointer-events-none"
      style={{
        background: 'url("data:image/svg+xml,%3Csvg viewBox=\'0 0 100 100\' xmlns=\'http://www.w3.org/2000/svg\'%3E%3Cfilter id=\'noise\'%3E%3CfeTurbulence type=\'fractalNoise\' baseFrequency=\'0.8\' numOctaves=\'3\' stitchTiles=\'stitch\'/%3E%3C/filter%3E%3Crect width=\'100\' height=\'100\' filter=\'url(%23noise)\' opacity=\'0.4\'/%3E%3C/svg%3E")',
        opacity: 0.4 * glitchIntensity,
        mixBlendMode: 'screen'
      }}
    />

    <div 
      className="fixed inset-0 z-99996 pointer-events-none"
      style={{
        backdropFilter: `blur(${3 * glitchIntensity}px)`,
        WebkitBackdropFilter: `blur(${3 * glitchIntensity}px)`,
        background: 'transparent'
      }}
    />

    <div 
      className="fixed top-1/2 left-1/2 transform -translate-x-1/2 -translate-y-1/2 z-99999 pointer-events-none text-center"
      style={{
        textShadow: '4px 4px 0 #ff00ff, -4px -4px 0 #00ffff',
        animation: `glitch-skew ${0.3 / glitchIntensity}s infinite`
      }}
    >
      <p className="text-4xl md:text-7xl font-display text-white whitespace-nowrap">
        {tabReturnMessage || 'TRACKING ERROR'}
      </p>
      <p className="text-sm font-mono text-neon-pink mt-4 animate-pulse">
        {Math.random() > 0.5 ? 'PLEASE REWIND' : 'INSERT TAPE'}
      </p>
      <p className="text-[8px] font-mono text-neon-blue/70 mt-2">
        ...
      </p>
    </div>

    <div className="fixed top-0 left-0 w-full h-40 z-99995 pointer-events-none"
      style={{
        background: 'linear-gradient(180deg, rgba(255,0,100,0.4) 0%, transparent 100%)',
        animation: `glitch-scan ${0.8 / glitchIntensity}s linear infinite`
      }}
    />
    <div className="fixed bottom-0 left-0 w-full h-40 z-99995 pointer-events-none"
      style={{
        background: 'linear-gradient(0deg, rgba(0,255,255,0.4) 0%, transparent 100%)',
        animation: `glitch-scan ${0.8 / glitchIntensity}s reverse infinite`
      }}
    />

    <div className="fixed inset-0 z-99994 pointer-events-none"
      style={{
        background: 'radial-gradient(circle at 50% 50%, rgba(255,255,255,0.1) 0%, transparent 80%)',
        animation: `pulse ${0.5 / glitchIntensity}s infinite`
      }}
    />
  </div>
)
}